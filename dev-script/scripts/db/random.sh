# MARK: 基础数据
# 生成随机创建时间
random_created_at() {
    local days="${1:-365}"
    local offset=$((RANDOM % (days * 86400)))
    local ts=$(($(date +%s) - offset))
    if [[ "$(uname)" == "Darwin" ]]; then
        date -u -r "$ts" '+%Y-%m-%d %H:%M:%S'
    else
        date -u -d "@$ts" '+%Y-%m-%d %H:%M:%S'
    fi
}
